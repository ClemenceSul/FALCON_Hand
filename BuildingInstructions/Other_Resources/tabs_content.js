function showTabContent(button, slideIndex = 0) {
    const data = button.id;
    const carousel_content = document.getElementById('step-slides');
    const carousel_dots = document.getElementById('carousel_dots');
    var tabButtons = document.querySelectorAll('.tab-button');

    // Remove 'active' class from all tab buttons
    tabButtons.forEach(function(button) {
        button.classList.remove('active');
    });
    button.classList.add('active');

    var parentDiv = button.parentNode;
    var parentId = parentDiv.id;

    function getQueryParameter(name) {
        const urlParams = new URLSearchParams(window.location.search);
        return urlParams.get(name);
    }
    const source = getQueryParameter('source');
    if (source == "I") {
        parentId = "I_" + parentId;
    }

    const tab_content = database[parentId][data];
    var set_image_type = localStorage.getItem('image_type');

    if (tab_content[set_image_type] == undefined) {
        set_image_type = "real";
    }
    var step_length = tab_content[set_image_type].length;

    for (var i = 1; i <= step_length; i++) {
        if (i==1) {
            carousel_content.innerHTML = `
                <div class="carousel-slide" style="display: block;">
                    <div style="display: flex;">
                        <p class="stepNB">1</p>
                        <img src=${tab_content[set_image_type][i-1]}>
                    </div>
                    <p class="stepText">${database[parentId][data]["text"][i-1]}</p>
                </div>
            `;
            carousel_dots.innerHTML = `
                <span class="dot" onclick="currentSlide(1)"></span>
            `;
        } else {
            carousel_content.innerHTML += `
                <div class="carousel-slide" style="display: block;">
                    <div style="display: flex;">
                        <p class="stepNB">${i}</p>
                        <img src=${tab_content[set_image_type][i-1]}>
                    </div>
                    <p class="stepText">${database[parentId][data]["text"][i-1]}</p>
                </div>
            `;
            carousel_dots.innerHTML += `
                <span class="dot" onclick="currentSlide(${i})"></span>
            `;
        }
    }
    showSlide(slideIndex);
}

